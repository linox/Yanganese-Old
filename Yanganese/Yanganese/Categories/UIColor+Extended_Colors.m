#import "UIColor+Extended_Colors.h"

@implementation UIColor (Extended_Colors)

+ (UIColor *)transparentBlack {
    return [UIColor colorWithRed:0.0 green:0.0 blue:0.0 alpha:0.7];
}

+ (UIColor *)lightTransparentBlack {
    return [UIColor colorWithRed:0.0 green:0.0 blue:0.0 alpha:0.3];
}

@end
